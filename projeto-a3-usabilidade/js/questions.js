function selectRandomQuestions(questions, numQuestions) {
    let selectedQuestions = [];
    let remainingQuestions = [...questions];
    for (let i = 0; i < numQuestions; i++) {
        const randomIndex = Math.floor(Math.random() * remainingQuestions.length);
        selectedQuestions.push(remainingQuestions[randomIndex]);
        remainingQuestions.splice(randomIndex, 1);
    }
    return selectedQuestions;
}

questions = selectRandomQuestions(questions, 2);

let currentQuestionIndex = 0;

let score = { value: 0 };

function displayQuestion() {
    if (currentQuestionIndex >= questions.length) {
        displayScore();
        return;
    }

    const quizContainer = document.getElementById('quiz-container');
    const question = questions[currentQuestionIndex];

    const questionHTML = `
        <div class="question-container">
            <h2 class="question">${question.question}</h2>
            <form id="question-form">
                ${question.answers.map((answer, idx) => `
                    <label class="answer-option">
                        <input type="radio" name="answer" value="${idx}" style="display: none;">
                        <div class="custom-radio"></div>
                        <span class="answer-text">${answer.option}</span>
                    </label>
                `).join('')}
            </form>
        </div>
    `;

    quizContainer.innerHTML = questionHTML;

    // Adicionando manipulador de evento para alternar o estilo quando a resposta for selecionada
    const answerOptions = document.querySelectorAll('.answer-option');
    answerOptions.forEach((option, index) => {
        option.addEventListener('click', () => {
            answerOptions.forEach((opt) => opt.classList.remove('selected'));
            option.classList.add('selected');
        });
    });
}

function checkAnswer() {
    const selectedAnswerIndex = document.querySelector('input[name="answer"]:checked');
    if (selectedAnswerIndex) {
        const answerIdx = parseInt(selectedAnswerIndex.value);
        const correctAnswer = questions[currentQuestionIndex].answers[answerIdx].correct;
        if (correctAnswer) {
            score.value++;
            alert("Resposta correta!");
        } else {
            alert("Resposta incorreta!");
        }
        currentQuestionIndex++;
        displayQuestion();

        if (currentQuestionIndex > 2) {
            window.location.href = "score.html";
        }
    } else {
        alert("Por favor, selecione uma resposta.");
    }
}

